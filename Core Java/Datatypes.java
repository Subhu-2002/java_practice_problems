public class Datatypes {
    public static void main(String[] args) {
        int num = 10;
        float cgpa = 5.5f;
        double per = 99.99;
        char ch = 'A';
        boolean isTrue = true;

        System.out.println(num);
        System.out.println(cgpa);
        System.out.println(per);
        System.out.println(ch);
        System.out.println(isTrue);
    }
}
